     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = request()->validate([
            'name' => 'required',
            'class' => 'required',
            'date_of_birth' => 'required',
            'department_id' => 'required',
            'image' => ''
        ]);

        Student::create($data);

        return redirect('students');
    }

